class DataProcessor:
    @staticmethod
    def calculate_returns(df, symbol):
        if df.empty or len(df) < 2:
            return None

        try:
            returns = {
                'symbol': symbol,
                'calculation_date': df.index[-1].date(),
                'return_1d': (df['Close'].iloc[-1] - df['Close'].iloc[-2]) / df['Close'].iloc[-2] if len(df) >= 2 else 0,
                'return_1w': (df['Close'].iloc[-1] - df['Close'].iloc[-5]) / df['Close'].iloc[-5] if len(df) >= 5 else 0,
                'return_1m': (df['Close'].iloc[-1] - df['Close'].iloc[-21]) / df['Close'].iloc[-21] if len(df) >= 21 else 0,
                'return_3m': (df['Close'].iloc[-1] - df['Close'].iloc[-63]) / df['Close'].iloc[-63] if len(df) >= 63 else 0,
                'return_6m': (df['Close'].iloc[-1] - df['Close'].iloc[-126]) / df['Close'].iloc[-126] if len(df) >= 126 else 0,
                'return_1y': (df['Close'].iloc[-1] - df['Close'].iloc[-252]) / df['Close'].iloc[-252] if len(df) >= 252 else 0,
                'return_3y': (df['Close'].iloc[-1] - df['Close'].iloc[-756]) / df['Close'].iloc[-756] if len(df) >= 756 else 0,
                'return_5y': (df['Close'].iloc[-1] - df['Close'].iloc[-1260]) / df['Close'].iloc[-1260] if len(df) >= 1260 else 0,
                'return_10y': (df['Close'].iloc[-1] - df['Close'].iloc[-2520]) / df['Close'].iloc[-2520] if len(df) >= 2520 else 0,
                'return_ytd': 0,
                'volatility_30d': df['Close'].pct_change().tail(30).std() * np.sqrt(252) if len(df) >= 30 else 0,
                'volatility_90d': df['Close'].pct_change().tail(90).std() * np.sqrt(252) if len(df) >= 90 else 0,
                'volatility_1y': df['Close'].pct_change().tail(252).std() * np.sqrt(252) if len(df) >= 252 else 0,
                'sharpe_ratio': 0,
                'max_drawdown': 0
            }

            current_year = df.index[-1].year
            ytd_data = df[df.index.year == current_year]
            if len(ytd_data) > 0:
                returns['return_ytd'] = (df['Close'].iloc[-1] - ytd_data['Close'].iloc[0]) / ytd_data['Close'].iloc[0]

            if len(df) >= 252:
                daily_returns = df['Close'].pct_change().tail(252)
                excess_returns = daily_returns - (0.02 / 252)
                if daily_returns.std() > 0:
                    returns['sharpe_ratio'] = (excess_returns.mean() / daily_returns.std()) * np.sqrt(252)

            rolling_max = df['Close'].cummax()
            drawdown = (df['Close'] - rolling_max) / rolling_max
            returns['max_drawdown'] = drawdown.min()

            return returns
        except:
            return None

    @staticmethod
    def calculate_correlations(price_dict):
        correlations = []
        price_df = pd.DataFrame()

        for symbol, df in price_dict.items():
            if not df.empty and 'close' in df.columns:
                price_df[symbol] = df.set_index('trading_date')['close']

        if not price_df.empty and len(price_df.columns) >= 2:
            symbols = price_df.columns.tolist()
            calc_date = price_df.index[-1] if len(price_df) > 0 else datetime.now().date()

            for i, sym1 in enumerate(symbols):
                for sym2 in symbols[i+1:]:
                    try:
                        corr_30d = price_df[[sym1, sym2]].tail(30).corr().iloc[0, 1] if len(price_df) >= 30 else 0
                        corr_90d = price_df[[sym1, sym2]].tail(90).corr().iloc[0, 1] if len(price_df) >= 90 else 0
                        corr_1y = price_df[[sym1, sym2]].tail(252).corr().iloc[0, 1] if len(price_df) >= 252 else 0
                        corr_3y = price_df[[sym1, sym2]].tail(756).corr().iloc[0, 1] if len(price_df) >= 756 else 0

                        correlations.append({
                            'symbol_1': sym1, 'symbol_2': sym2, 'calculation_date': calc_date,
                            'correlation_30d': corr_30d, 'correlation_90d': corr_90d,
                            'correlation_1y': corr_1y, 'correlation_3y': corr_3y
                        })
                    except:
                        pass

        return correlations

    @staticmethod
    def calculate_sector_performance(all_data):
        sector_perf = []

        try:
            quotes_df = pd.DataFrame(all_data['quotes'])
            fundamentals_df = pd.DataFrame(all_data['fundamentals'])
            stock_info_df = pd.DataFrame(all_data['stock_info'])

            if quotes_df.empty or stock_info_df.empty:
                return sector_perf

            merged = quotes_df.merge(stock_info_df[['symbol', 'sector']], on='symbol', how='left')

            if 'market_cap' not in merged.columns and not fundamentals_df.empty and 'market_cap' in fundamentals_df.columns:
                merged = merged.merge(fundamentals_df[['symbol', 'market_cap']], on='symbol', how='left', suffixes=('', '_fund'))

            if not fundamentals_df.empty and 'trailing_pe' in fundamentals_df.columns:
                merged = merged.merge(fundamentals_df[['symbol', 'trailing_pe']], on='symbol', how='left', suffixes=('', '_pe'))

            for sector in merged['sector'].unique():
                if pd.isna(sector) or sector == 'Unknown' or sector == '':
                    continue

                sector_data = merged[merged['sector'] == sector]

                avg_market_cap = 0
                if 'market_cap' in sector_data.columns:
                    avg_market_cap = sector_data['market_cap'].mean()

                avg_pe = 0
                if 'trailing_pe' in sector_data.columns:
                    avg_pe = sector_data['trailing_pe'].mean()

                avg_change = 0
                if 'change_percent' in sector_data.columns:
                    avg_change = sector_data['change_percent'].mean()

                total_vol = 0
                if 'volume' in sector_data.columns:
                    total_vol = sector_data['volume'].sum()

                best_performer = ''
                worst_performer = ''
                if 'change_percent' in sector_data.columns and len(sector_data) > 0:
                    try:
                        best_performer = sector_data.loc[sector_data['change_percent'].idxmax(), 'symbol']
                        worst_performer = sector_data.loc[sector_data['change_percent'].idxmin(), 'symbol']
                    except:
                        pass

                sector_perf.append({
                    'sector': sector,
                    'calculation_date': datetime.now().date(),
                    'avg_price_change': float(avg_change) if pd.notna(avg_change) else 0,
                    'avg_return_1m': 0,
                    'avg_return_1y': 0,
                    'avg_market_cap': int(avg_market_cap) if pd.notna(avg_market_cap) else 0,
                    'avg_pe_ratio': float(avg_pe) if pd.notna(avg_pe) else 0,
                    'total_volume': int(total_vol) if pd.notna(total_vol) else 0,
                    'num_stocks': len(sector_data),
                    'best_performer': best_performer,
                    'worst_performer': worst_performer
                })
        except Exception as e:
            logger.warning(f"Sector performance calculation error: {e}")

        return sector_perf

