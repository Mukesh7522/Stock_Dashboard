class DatabaseManager:
    def __init__(self, config):
        self.config = config
        self.conn = None

    def connect(self):
        try:
            self.conn = psycopg2.connect(**self.config)
            logger.info("✅ Database connected successfully")
            return True
        except Exception as e:
            logger.error(f"❌ Database connection failed: {e}")
            return False

    def disconnect(self):
        if self.conn:
            self.conn.close()
            logger.info("🔌 Database disconnected")

    def execute_query(self, query, params=None):
        try:
            cursor = self.conn.cursor()
            cursor.execute(query, params)
            self.conn.commit()
            cursor.close()
            return True
        except Exception as e:
            logger.error(f"Query execution error: {e}")
            self.conn.rollback()
            return False

    def fetch_query(self, query, params=None):
        """Fetch query results"""
        try:
            cursor = self.conn.cursor()
            cursor.execute(query, params)
            results = cursor.fetchall()
            cursor.close()
            return results
        except Exception as e:
            logger.error(f"Fetch query error: {e}")
            return []

    def batch_insert(self, query, data, page_size=1000):
        if not data:
            return 0
        try:
            cursor = self.conn.cursor()
            execute_batch(cursor, query, data, page_size=page_size)
            self.conn.commit()
            cursor.close()
            return len(data)
        except Exception as e:
            logger.error(f"Batch insert error: {e}")
            self.conn.rollback()
            return 0

    def verify_table_data(self, table_name):
        """Verify data in a specific table"""
        query = f"SELECT COUNT(*) FROM {table_name}"
        try:
            cursor = self.conn.cursor()
            cursor.execute(query)
            count = cursor.fetchone()[0]
            cursor.close()
            return count
        except Exception as e:
            logger.error(f"Verification error for {table_name}: {e}")
            return 0

