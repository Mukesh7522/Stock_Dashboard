class StockDataCollector:
    def __init__(self, config):
        self.config = config
        self.ta = TechnicalAnalysis()

    def download_logo(self, symbol):
        try:
            domain = self.config.COMPANY_DOMAINS.get(symbol, '')
            if not domain:
                return None, None

            logo_url = f"https://logo.clearbit.com/{domain}"
            response = requests.get(logo_url, timeout=10)

            if response.status_code == 200:
                img = Image.open(BytesIO(response.content))
                img = img.convert('RGBA')
                img = img.resize((128, 128), Image.Resampling.LANCZOS)

                buffered = BytesIO()
                img.save(buffered, format="PNG")
                img_str = base64.b64encode(buffered.getvalue()).decode('utf-8')

                return f"logo_{symbol}.png", img_str

            return None, None
        except:
            return None, None

    def get_dividend_split_history(self, stock, symbol):
        dividends_list = []
        splits_list = []

        try:
            dividends = stock.dividends
            if not dividends.empty:
                for date, amount in dividends.items():
                    if date.year >= 2015:
                        dividends_list.append({
                            'symbol': symbol,
                            'ex_date': date.date(),
                            'dividend_amount': float(amount),
                            'year': date.year,
                            'quarter': (date.month - 1) // 3 + 1
                        })

            splits = stock.splits
            if not splits.empty:
                for date, ratio in splits.items():
                    if date.year >= 2015:
                        splits_list.append({
                            'symbol': symbol,
                            'split_date': date.date(),
                            'split_ratio': float(ratio),
                            'split_description': f"{int(ratio)}:1" if ratio > 1 else f"1:{int(1/ratio)}"
                        })
        except:
            pass

        return dividends_list, splits_list

    def create_monthly_returns_heatmap(self, df, symbol):
        if df.empty:
            return []

        monthly_returns = []

        try:
            df_copy = df.copy()
            df_copy['year'] = df_copy.index.year
            df_copy['month'] = df_copy.index.month

            for year in range(2015, 2026):
                year_data = df_copy[df_copy['year'] == year]

                for month in range(1, 13):
                    month_data = year_data[year_data['month'] == month]

                    if len(month_data) > 0:
                        month_return = (month_data['Close'].iloc[-1] - month_data['Close'].iloc[0]) / month_data['Close'].iloc[0]

                        monthly_returns.append({
                            'symbol': symbol,
                            'year': year,
                            'month': month,
                            'month_name': pd.to_datetime(f'{year}-{month:02d}-01').strftime('%b'),
                            'return_pct': month_return * 100,
                            'start_price': month_data['Close'].iloc[0],
                            'end_price': month_data['Close'].iloc[-1]
                        })
        except:
            pass

        return monthly_returns

    def collect_stock_data(self, symbol):
        for attempt in range(self.config.MAX_RETRIES):
            try:
                stock = yf.Ticker(symbol)
                info = stock.info

                hist = stock.history(period=self.config.HISTORY_PERIOD)

                if hist.empty:
                    raise ValueError(f"No historical data for {symbol}")

                income_stmt = stock.quarterly_financials
                balance_sheet = stock.quarterly_balance_sheet
                cashflow = stock.quarterly_cashflow

                dividends, splits = self.get_dividend_split_history(stock, symbol)

                logo_path, logo_base64 = self.download_logo(symbol)

                hist = self.ta.calculate_indicators(hist)

                support, resistance = self.ta.calculate_support_resistance(hist)

                signals = self.ta.generate_trading_signals(hist)

                crossovers = self.ta.detect_ma_crossovers(hist)

                monthly_returns = self.create_monthly_returns_heatmap(hist, symbol)

                return {
                    'symbol': symbol,
                    'info': info,
                    'history': hist,
                    'income_statement': income_stmt,
                    'balance_sheet': balance_sheet,
                    'cashflow': cashflow,
                    'dividends': dividends,
                    'splits': splits,
                    'support_levels': support,
                    'resistance_levels': resistance,
                    'trading_signals': signals,
                    'ma_crossovers': crossovers,
                    'monthly_returns': monthly_returns,
                    'logo_path': logo_path,
                    'logo_base64': logo_base64,
                    'success': True
                }

            except Exception as e:
                if attempt < self.config.MAX_RETRIES - 1:
                    logger.warning(f"⚠️ {symbol} attempt {attempt + 1} failed, retrying...")
                    time.sleep(self.config.RETRY_DELAY)
                    continue
                else:
                    logger.error(f"❌ {symbol} failed after {self.config.MAX_RETRIES} attempts: {e}")
                    return {'symbol': symbol, 'success': False}

        return {'symbol': symbol, 'success': False}

