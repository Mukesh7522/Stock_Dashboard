    def _extract_financial_statements(self, symbol, data, all_data):
        if data['income_statement'] is not None and not data['income_statement'].empty:
            income_df = data['income_statement'].T
            for idx, row in income_df.iterrows():
                try:
                    total_rev = row.get('Total Revenue', row.get('TotalRevenue', 0))
                    cost_rev = row.get('Cost Of Revenue', row.get('CostOfRevenue', 0))
                    gross_profit = row.get('Gross Profit', row.get('GrossProfit', 0))
                    op_income = row.get('Operating Income', row.get('OperatingIncome', 0))
                    net_income = row.get('Net Income', row.get('NetIncome', 0))
                    ebitda = row.get('EBITDA', row.get('Ebitda', 0))

                    all_data['income_statements'].append({
                        'symbol': symbol,
                        'fiscal_date': pd.to_datetime(idx).date(),
                        'period': 'Quarterly',
                        'total_revenue': int(total_rev) if pd.notna(total_rev) else 0,
                        'cost_of_revenue': int(cost_rev) if pd.notna(cost_rev) else 0,
                        'gross_profit': int(gross_profit) if pd.notna(gross_profit) else 0,
                        'research_development': int(row.get('Research And Development', 0)) if pd.notna(row.get('Research And Development')) else 0,
                        'selling_general_admin': int(row.get('Selling General And Administration', 0)) if pd.notna(row.get('Selling General And Administration')) else 0,
                        'operating_expenses': int(row.get('Operating Expense', 0)) if pd.notna(row.get('Operating Expense')) else 0,
                        'operating_income': int(op_income) if pd.notna(op_income) else 0,
                        'interest_expense': int(row.get('Interest Expense', 0)) if pd.notna(row.get('Interest Expense')) else 0,
                        'other_income_expense': 0,
                        'income_before_tax': int(row.get('Income Before Tax', 0)) if pd.notna(row.get('Income Before Tax')) else 0,
                        'income_tax_expense': int(row.get('Tax Provision', 0)) if pd.notna(row.get('Tax Provision')) else 0,
                        'net_income': int(net_income) if pd.notna(net_income) else 0,
                        'ebitda': int(ebitda) if pd.notna(ebitda) else 0,
                        'basic_eps': 0,
                        'diluted_eps': 0,
                        'gross_margin': float(gross_profit / total_rev) if total_rev > 0 else 0,
                        'operating_margin': float(op_income / total_rev) if total_rev > 0 else 0,
                        'net_margin': float(net_income / total_rev) if total_rev > 0 else 0,
                        'ebitda_margin': float(ebitda / total_rev) if total_rev > 0 else 0
                    })
                except:
                    pass

        if data['balance_sheet'] is not None and not data['balance_sheet'].empty:
            balance_df = data['balance_sheet'].T
            for idx, row in balance_df.iterrows():
                try:
                    total_assets = row.get('Total Assets', row.get('TotalAssets', 0))
                    current_assets = row.get('Current Assets', row.get('CurrentAssets', 0))
                    total_liabilities = row.get('Total Liabilities Net Minority Interest', 0)
                    current_liabilities = row.get('Current Liabilities', row.get('CurrentLiabilities', 0))
                    total_equity = row.get('Total Equity Gross Minority Interest', row.get('StockholdersEquity', 0))
                    long_term_debt = row.get('Long Term Debt', row.get('LongTermDebt', 0))
                    short_term_debt = row.get('Current Debt', row.get('CurrentDebt', 0))
                    cash = row.get('Cash And Cash Equivalents', row.get('CashAndCashEquivalents', 0))

                    total_debt = (long_term_debt if pd.notna(long_term_debt) else 0) + (short_term_debt if pd.notna(short_term_debt) else 0)

                    all_data['balance_sheets'].append({
                        'symbol': symbol,
                        'fiscal_date': pd.to_datetime(idx).date(),
                        'period': 'Quarterly',
                        'total_assets': int(total_assets) if pd.notna(total_assets) else 0,
                        'current_assets': int(current_assets) if pd.notna(current_assets) else 0,
                        'cash_and_equivalents': int(cash) if pd.notna(cash) else 0,
                        'accounts_receivable': int(row.get('Accounts Receivable', 0)) if pd.notna(row.get('Accounts Receivable')) else 0,
                        'inventory': int(row.get('Inventory', 0)) if pd.notna(row.get('Inventory')) else 0,
                        'property_plant_equipment': int(row.get('Net PPE', 0)) if pd.notna(row.get('Net PPE')) else 0,
                        'goodwill': int(row.get('Goodwill', 0)) if pd.notna(row.get('Goodwill')) else 0,
                        'total_liabilities': int(total_liabilities) if pd.notna(total_liabilities) else 0,
                        'current_liabilities': int(current_liabilities) if pd.notna(current_liabilities) else 0,
                        'accounts_payable': int(row.get('Accounts Payable', 0)) if pd.notna(row.get('Accounts Payable')) else 0,
                        'short_term_debt': int(short_term_debt) if pd.notna(short_term_debt) else 0,
                        'long_term_debt': int(long_term_debt) if pd.notna(long_term_debt) else 0,
                        'total_equity': int(total_equity) if pd.notna(total_equity) else 0,
                        'retained_earnings': int(row.get('Retained Earnings', 0)) if pd.notna(row.get('Retained Earnings')) else 0,
                        'working_capital': int(current_assets - current_liabilities) if pd.notna(current_assets) and pd.notna(current_liabilities) else 0,
                        'total_debt': int(total_debt),
                        'net_debt': int(total_debt - cash) if pd.notna(cash) else int(total_debt),
                        'current_ratio': float(current_assets / current_liabilities) if current_liabilities > 0 else 0,
                        'debt_to_equity': float(total_debt / total_equity) if total_equity > 0 else 0,
                        'debt_to_assets': float(total_debt / total_assets) if total_assets > 0 else 0
                    })
                except:
                    pass

        if data['cashflow'] is not None and not data['cashflow'].empty:
            cashflow_df = data['cashflow'].T
            for idx, row in cashflow_df.iterrows():
                try:
                    operating_cf = row.get('Operating Cash Flow', row.get('OperatingCashFlow', 0))
                    capex = row.get('Capital Expenditure', row.get('CapitalExpenditure', 0))

                    all_data['cashflows'].append({
                        'symbol': symbol,
                        'fiscal_date': pd.to_datetime(idx).date(),
                        'period': 'Quarterly',
                        'operating_cashflow': int(operating_cf) if pd.notna(operating_cf) else 0,
                        'investing_cashflow': int(row.get('Investing Cash Flow', 0)) if pd.notna(row.get('Investing Cash Flow')) else 0,
                        'financing_cashflow': int(row.get('Financing Cash Flow', 0)) if pd.notna(row.get('Financing Cash Flow')) else 0,
                        'net_change_in_cash': int(row.get('Changes In Cash', 0)) if pd.notna(row.get('Changes In Cash')) else 0,
                        'capital_expenditures': int(capex) if pd.notna(capex) else 0,
                        'free_cashflow': int(operating_cf - abs(capex)) if pd.notna(operating_cf) and pd.notna(capex) else 0,
                        'dividends_paid': int(row.get('Cash Dividends Paid', 0)) if pd.notna(row.get('Cash Dividends Paid')) else 0,
                        'stock_repurchased': int(row.get('Repurchase Of Capital Stock', 0)) if pd.notna(row.get('Repurchase Of Capital Stock')) else 0,
                        'depreciation': int(row.get('Depreciation And Amortization', 0)) if pd.notna(row.get('Depreciation And Amortization')) else 0,
                        'fcf_margin': 0
                    })
                except:
                    pass

