class TechnicalAnalysis:
    @staticmethod
    def calculate_indicators(df):
        if df.empty or len(df) < 2:
            return df

        try:
            # Moving Averages
            df['ma_20'] = df['Close'].rolling(window=20, min_periods=1).mean()
            df['ma_50'] = df['Close'].rolling(window=50, min_periods=1).mean()
            df['ma_200'] = df['Close'].rolling(window=200, min_periods=1).mean()
            df['ema_12'] = df['Close'].ewm(span=12, adjust=False).mean()
            df['ema_26'] = df['Close'].ewm(span=26, adjust=False).mean()

            # RSI
            delta = df['Close'].diff()
            gain = delta.where(delta > 0, 0).rolling(window=14, min_periods=1).mean()
            loss = -delta.where(delta < 0, 0).rolling(window=14, min_periods=1).mean()
            rs = gain / loss.replace(0, np.nan)
            df['rsi_14'] = 100 - (100 / (1 + rs))

            # MACD
            df['macd'] = df['ema_12'] - df['ema_26']
            df['macd_signal'] = df['macd'].ewm(span=9, adjust=False).mean()
            df['macd_histogram'] = df['macd'] - df['macd_signal']

            # Bollinger Bands
            df['bollinger_middle'] = df['Close'].rolling(window=20, min_periods=1).mean()
            std = df['Close'].rolling(window=20, min_periods=1).std()
            df['bollinger_upper'] = df['bollinger_middle'] + (std * 2)
            df['bollinger_lower'] = df['bollinger_middle'] - (std * 2)
            df['bollinger_width'] = df['bollinger_upper'] - df['bollinger_lower']

            # Stochastic
            low_14 = df['Low'].rolling(window=14, min_periods=1).min()
            high_14 = df['High'].rolling(window=14, min_periods=1).max()
            df['stochastic_k'] = 100 * ((df['Close'] - low_14) / (high_14 - low_14).replace(0, np.nan))
            df['stochastic_d'] = df['stochastic_k'].rolling(window=3, min_periods=1).mean()

            # ATR
            high_low = df['High'] - df['Low']
            high_close = np.abs(df['High'] - df['Close'].shift())
            low_close = np.abs(df['Low'] - df['Close'].shift())
            tr = pd.concat([high_low, high_close, low_close], axis=1).max(axis=1)
            df['atr_14'] = tr.rolling(window=14, min_periods=1).mean()

            # OBV & VWAP
            df['obv'] = (np.sign(df['Close'].diff()) * df['Volume']).fillna(0).cumsum()
            df['vwap'] = (df['Close'] * df['Volume']).cumsum() / df['Volume'].cumsum()

            # Volatility & Returns
            df['volatility_30'] = df['Close'].pct_change().rolling(window=30, min_periods=1).std() * np.sqrt(252)
            df['daily_return'] = df['Close'].pct_change()

            # Volume
            df['avg_volume_20'] = df['Volume'].rolling(window=20, min_periods=1).mean()
            df['volume_ratio'] = df['Volume'] / df['avg_volume_20']

            # Momentum
            df['momentum_10'] = df['Close'].pct_change(periods=10)
            df['momentum_20'] = df['Close'].pct_change(periods=20)

            df = df.fillna(0)
        except Exception as e:
            logger.warning(f"Technical indicators error: {e}")

        return df

    @staticmethod
    def calculate_support_resistance(df, window=20):
        try:
            if len(df) < window:
                return [], []

            high_idx = argrelextrema(df['High'].values, np.greater, order=window)[0]
            low_idx = argrelextrema(df['Low'].values, np.less, order=window)[0]

            resistance = df.iloc[high_idx]['High'].tail(5).tolist() if len(high_idx) > 0 else []
            support = df.iloc[low_idx]['Low'].tail(5).tolist() if len(low_idx) > 0 else []

            return support, resistance
        except:
            return [], []

    @staticmethod
    def generate_trading_signals(df):
        signals = []

        try:
            if len(df) < 50:
                return signals

            latest = df.iloc[-1]
            prev = df.iloc[-2]

            if latest['rsi_14'] < 30:
                signals.append(('BUY', 'RSI Oversold', latest.name, latest['rsi_14']))
            elif latest['rsi_14'] > 70:
                signals.append(('SELL', 'RSI Overbought', latest.name, latest['rsi_14']))

            if prev['macd'] < prev['macd_signal'] and latest['macd'] > latest['macd_signal']:
                signals.append(('BUY', 'MACD Bullish Crossover', latest.name, latest['macd']))
            elif prev['macd'] > prev['macd_signal'] and latest['macd'] < latest['macd_signal']:
                signals.append(('SELL', 'MACD Bearish Crossover', latest.name, latest['macd']))

            if prev['ma_20'] < prev['ma_50'] and latest['ma_20'] > latest['ma_50']:
                signals.append(('BUY', 'Golden Cross (20/50)', latest.name, latest['ma_20']))
            elif prev['ma_20'] > prev['ma_50'] and latest['ma_20'] < latest['ma_50']:
                signals.append(('SELL', 'Death Cross (20/50)', latest.name, latest['ma_20']))

            if latest['Close'] < latest['bollinger_lower']:
                signals.append(('BUY', 'Price Below Lower Band', latest.name, latest['Close']))
            elif latest['Close'] > latest['bollinger_upper']:
                signals.append(('SELL', 'Price Above Upper Band', latest.name, latest['Close']))
        except:
            pass

        return signals

    @staticmethod
    def detect_ma_crossovers(df):
        crossovers = []

        try:
            if len(df) < 200:
                return crossovers

            recent_df = df.tail(504)

            for i in range(1, len(recent_df)):
                curr = recent_df.iloc[i]
                prev = recent_df.iloc[i-1]

                if prev['ma_50'] <= prev['ma_200'] and curr['ma_50'] > curr['ma_200']:
                    crossovers.append({
                        'date': curr.name, 'type': 'Golden Cross',
                        'ma_short': 50, 'ma_long': 200, 'price': curr['Close']
                    })
                elif prev['ma_50'] >= prev['ma_200'] and curr['ma_50'] < curr['ma_200']:
                    crossovers.append({
                        'date': curr.name, 'type': 'Death Cross',
                        'ma_short': 50, 'ma_long': 200, 'price': curr['Close']
                    })

                if prev['ma_20'] <= prev['ma_50'] and curr['ma_20'] > curr['ma_50']:
                    crossovers.append({
                        'date': curr.name, 'type': 'Bullish Cross',
                        'ma_short': 20, 'ma_long': 50, 'price': curr['Close']
                    })
                elif prev['ma_20'] >= prev['ma_50'] and curr['ma_20'] < curr['ma_50']:
                    crossovers.append({
                        'date': curr.name, 'type': 'Bearish Cross',
                        'ma_short': 20, 'ma_long': 50, 'price': curr['Close']
                    })
        except:
            pass

        return crossovers

